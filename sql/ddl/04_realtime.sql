-- Enable Realtime for chat tables (safe to re-run).
-- Dashboard: Database → Publications → supabase_realtime should list these tables.
-- The app uses private Realtime channels so postgres_changes respect RLS; JWT must be a logged-in user.

do $$
begin
  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'messages'
  ) then
    alter publication supabase_realtime add table public.messages;
  end if;

  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'conversations'
  ) then
    alter publication supabase_realtime add table public.conversations;
  end if;
end $$;
